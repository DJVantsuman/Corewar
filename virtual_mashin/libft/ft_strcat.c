char	*ft_strcat(char *d, char *s)
{
	int i;
	int	n;

	i = 0;
	n = 0;
	while (d[i] != '\0')
		i++;
	while (s[n] != '\0')
	{
		d[i] = s[n];
		i++;
		n++;
	}
	d[i] = '\0';
	return (d);
}
