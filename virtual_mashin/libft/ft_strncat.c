#include <string.h>

char	*ft_strncat(char *d, const char *s, size_t count)
{
	int		i;
	size_t	n;

	i = 0;
	n = 0;
	while (d[i] != '\0')
		i++;
	while (n < count && s[n] != '\0')
	{
		d[i] = s[n];
		i++;
		n++;
	}
	d[i] = '\0';
	return (d);
}
