#include "libft.h"

int		ft_atoi(const char *s)
{
	size_t					i;
	unsigned long long int	n;
	int						f;

	i = 0;
	n = 0;
	f = 1;
	while ((s[i] > 8 && s[i] < 14) || s[i] == 32)
		i++;
	if (s[i] == '+')
		i++;
	else if (s[i] == '-')
	{
		f = -1;
		i++;
	}
	while (s[i] >= 48 && s[i] <= 57)
	{
		n = n * 10 + (s[i++] - '0');
		if (n > 9223372036854775807 && f == 1)
			return (-1);
		else if (n > 9223372036854775807 && f == -1)
			return (0);
	}
	return (n * f);
}
